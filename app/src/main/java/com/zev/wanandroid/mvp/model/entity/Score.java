package com.zev.wanandroid.mvp.model.entity;

public class Score {

    private int rank;
    private int userId;
    private int level;
    private int coinCount;
    private String username;


    public int getRank() {
        return rank;
    }

    public int getUserId() {
        return userId;
    }

    public int getLevel() {
        return level;
    }

    public int getCoinCount() {
        return coinCount;
    }

    public String getUsername() {
        return username;
    }
}
